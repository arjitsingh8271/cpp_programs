# include <iostream>
using namespace std;

int main()
{
	int a = 5;
	float b = 1.2f;
	double c = 3.33;
	string d = "Hello";
	char e = 'A';
	bool f = true;

	cout<<"int a: "<<a<<endl;
	cout<<"float b: "<<b<<endl;
	cout<<"double c: "<<c<<endl;
	cout<<"string d: "<<d<<endl;
	cout<<"char e: "<<e<<endl;
	cout<<"bool f: "<<f<<endl;
	

	return 0;
}



/*
OUTPUT:	int a: 5
		float b: 1.2
		double c: 3.33
		string d: Hello
		char e: A
		bool f: 1
*/